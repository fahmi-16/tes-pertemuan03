package operator;

public class Switch {
    public static void main(String[] args) {
        int jalanjalan=3;
        switch (jalanjalan){
            case 1:
                System.out.println("urang lapas");
                break;
            case 2:
                System.out.println("urang acara");
                break;
            case 3:
                System.out.println("ubat nyamuk");
                break;
            case 4:
                System.out.println("dubil dit");
                break;
            default:
                System.out.println("makhluk halus");
                break;
        }
    }
}
